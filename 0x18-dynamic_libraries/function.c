#include "main.h"

/**
 * sum - addition operator
 * @a: first int
 * @b: second int
 * Return: sum of 2 numbers
 */

int sum(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction operator
 * @a: first int
 * @b: secong int
 * Return: difference of 2 numbers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication operator
 * @a: first int
 * @b: second int
 * Return: multiplication of 2 numbers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division operator
 * @a: first int
 * @b: second int
 * Return: division of 2 numbers
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus operator
 * @a: first int
 * @b: second int
 * Return: reminder of division of 2 numbers
 */

int mod(int a, int b)
{
	return (a % b);
}
